     */
    public function edit(Request $request)
    {
        $document = $request->document;
        $id = DB::table('students')
            ->select('id')
            ->where('document', '=', $document)
            ->value('id');

        $studentData = request()->except(['_token']);
        Student::where('id', '=', $id)->update($studentData);

        redirect('student');
    }

